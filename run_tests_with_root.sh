#!/bin/bash

################################################################################
# 需要root权限的单元测试启动脚本
#
# 功能：
#   - 清理旧的共享内存（可能需要root权限）
#   - 编译所有测试
#   - 执行所有单元测试
#   - 生成测试报告
#
# 用法：
#   sudo ./run_tests_with_root.sh [选项]
#
# 选项：
#   --clean         清理旧的共享内存并退出
#   --no-build      跳过编译，直接运行测试
#   --verbose       详细输出
#   --test NAME    只运行指定的测试
#   --list         列出所有可用的测试
#
################################################################################

# 检查是否为root权限
if [ "$EUID" -ne 0 ]; then
    echo "此脚本需要root权限运行"
    echo "请使用: sudo $0 $@"
    exit 1
fi

# 脚本目录
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# 调用主测试脚本
exec "${SCRIPT_DIR}/run_all_tests.sh" "$@"
